<template>
  <div>
    <p>いいね {{ number }}</p>
    <button @click="increment()">+1</button>
  </div>
</template>

<script>
export default{
  data: function() {
    return {
      number: 4
    }
  },
  methods: {
    increment: function(){
      this.number += 1;
    }
  }
}
</script>

<style scoped>
div {
  border: 1px solid blue;
}
</style>